"""
Approach B: BF Partition Function Analysis

Park & Park (2602.19621) Proposition 6.5 gives the KEY formula:

    Z_X = |pi(Sel(M, W))| * |Sel(M_1^v, W_1^perp)|

For the case of an elliptic curve E/Q with the n-isogeny sequence
    0 -> E[phi] -> E[n*phi] -> E[n] -> 0
the partition function becomes:

    Z_X = |im(Sel_{phi}(E) -> Sel_n(E))| * |Sel_{phi^hat}(E')|

where phi: E -> E' is the isogeny and phi^hat is the dual.

KEY QUESTION: Does finiteness of Z_X IMPLY finiteness of Sha?
Or does Z_X's finiteness follow FROM Sha being finite?

ANALYSIS:
"""

