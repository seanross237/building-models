print("""
Park & Park Proposition 6.5 (the partition function formula):

    Z_X = |pi(Sel(M, W))| * |Sel(M_1^v, W_1^perp)|

For M = E[n] with the sequence 0 -> M_1 -> M -> M_2 -> 0:

    Z_X = |pi(Sel(M, W))| * |Sel(M_1^v, W_1^perp)|

Both factors are SELMER GROUP sizes, which are ALWAYS FINITE 
(by Remark 5.4, which cites Cesnavicius Theorem 3.2).

So Z_X is ALWAYS finite. This is automatic from:
  - Selmer groups are finite (proved by Cassels, 1960s)
  - The partition function is a product of two finite Selmer groups

CRUCIAL OBSERVATION:
  Z_X being finite does NOT prove Sha is finite!
  
  The relationship is:
    Sha[n] = ker(Sel_n(E) -> prod_v E(Q_v)/nE(Q_v))
           = Sel_n(E) / im(E(Q)/nE(Q))
  
  So |Sha[n]| = |Sel_n(E)| / |E(Q)/nE(Q)| (when these are all finite)
  
  |Sha[n]| = |Sel_n(E)| / n^rank * (torsion correction)
  
  But Z_X involves Sel(M, W) and Sel(M_1^v, W_1^perp), which are 
  DIFFERENT Selmer groups from Sel_n(E). Specifically:
  
  - Sel(M, W) is the Selmer group for the FULL module M = E[n]
    with the standard Selmer conditions W
  - Sel(M_1^v, W_1^perp) is the Selmer group for the DUAL module M_1^v
    with the ORTHOGONAL Selmer conditions
  
  Both are finite BY DEFINITION of Selmer groups.

DIRECTION OF IMPLICATION:
  Z_X finite <== Selmer groups finite (always true)
  
  NOT:
  Z_X finite ==> Sha finite
  
  The BF partition function is finite REGARDLESS of whether Sha is finite.
  The issue is that Sha = Sel/E(Q)/nE(Q), and while Sel is finite,
  E(Q)/nE(Q) is also finite (since E(Q) is finitely generated by Mordell-Weil).
  
  So Sha[n] is ALWAYS a quotient of two finite groups, hence finite.
  BUT: This is only Sha[n], the n-torsion. The FULL Sha is 
  the direct limit of Sha[n] over all n. Sha is finite iff 
  Sha[n] = Sha[n^2] for all sufficiently large n (i.e., stabilization).

THE REAL PROBLEM:
  We know: dim_Fp Sha[p^k] is finite for every fixed k.
  We want: Sha[p^infty] := union_k Sha[p^k] is finite.
  
  Equivalently: Sha[p^k] stabilizes as k -> infinity.
  
  The BF theory gives Z_{X,n} for each n. As n = p^k grows:
    Z_{X,p^k} = |pi(Sel_{p^k}(M, W))| * |Sel(M_1^v, W_1^perp)|
  
  Sha is finite iff the Selmer groups |Sel_{p^k}| grow like p^{2k*rank}
  (the E(Q)/p^k E(Q) part grows like p^{k*rank}, and Sha[p^k] stabilizes).
  
  If Sha[p^infty] is infinite, then |Sel_{p^k}| grows FASTER than p^{2k*rank}.
  
  Can the BF theory detect this? The partition function Z is a sum of phases:
    Z_X = sum_{rho in F(X)} exp(2*pi*i * BF_X(rho))
  
  If Sha is infinite, F(X) is still finite (for fixed n), but as n -> infty,
  |F(X)| = |Sel_n| grows. The BF theory would need to detect the GROWTH RATE
  of |Sel_{p^k}|, which relates to Iwasawa theory.

CONCLUSION FOR APPROACH B:
  The BF partition function Z_X is ALWAYS finite (a product of finite 
  Selmer groups). It does not provide new information about Sha finiteness.
  
  However, the GROWTH of Z_{X,p^k} as k -> infinity IS related to Sha 
  finiteness via Iwasawa theory. If Z_{X,p^k} ~ C * p^{2k*rank} as k -> inf,
  then Sha is finite. If it grows faster, Sha[p^infty] is infinite.
  
  The BF theory repackages the Iwasawa-theoretic question:
    Is the Iwasawa mu-invariant of E zero?
    (mu = 0 iff Sha[p^infty] is finite, for p of good ordinary reduction,
     assuming the Iwasawa main conjecture)
  
  Park & Park's contribution is a STRUCTURAL result (Cassels-Tate = BF 
  functional) not a FINITENESS result.
""")

